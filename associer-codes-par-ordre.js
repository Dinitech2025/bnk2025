const { ocrSpace } = require('ocr-space-api-wrapper');
const pdf = require('pdf-poppler');
const fs = require('fs');
const path = require('path');

// Configuration OCR
const API_KEY_OCR = 'K86617027088957';

async function associerCodesParOrdre() {
    console.log('üéØ ASSOCIATION DES CODES PAR ORDRE S√âQUENTIEL');
    console.log('üìã Logique: Les codes apparaissent dans l\'ordre 1, 2, 3... m√™me si les num√©ros ne sont pas d√©tect√©s');
    console.log('üîç Nous allons extraire tous les codes dans l\'ordre et les associer aux num√©ros 1-321\n');
    
    const pdfPath = 'pdfs/500Ar2.pdf';
    
    try {
        // Configuration standard
        let options = {
            format: 'png',
            out_dir: './temp/',
            out_prefix: 'ordre_analysis',
            page: null,
            density: 600,
            width: 5100,
            height: 6600
        };
        
        console.log('üñºÔ∏è Conversion PDF en images...');
        await pdf.convert(pdfPath, options);
        
        const tempFiles = fs.readdirSync('./temp/').filter(f => f.startsWith('ordre_analysis'));
        console.log(`üì∏ ${tempFiles.length} images g√©n√©r√©es\n`);
        
        let tousLesCodesOrdonnes = [];  // Tous les codes dans l'ordre d'apparition
        let numerosDetectes = [];       // Num√©ros s√©par√©s d√©tect√©s
        
        // OCR page par page pour extraire tous les codes
        for (let i = 0; i < tempFiles.length; i++) {
            const imagePath = path.join('./temp/', tempFiles[i]);
            const pageNum = i + 1;
            
            console.log(`üîç EXTRACTION Page ${pageNum}/${tempFiles.length}:`);
            
            try {
                const ocrResult = await ocrSpace(imagePath, {
                    apiKey: API_KEY_OCR,
                    language: 'eng',
                    isOverlayRequired: false,
                    detectOrientation: true,
                    scale: true,
                    isTable: false,
                    OCREngine: 2
                });
                
                if (ocrResult && ocrResult.ParsedResults && ocrResult.ParsedResults[0]) {
                    const text = ocrResult.ParsedResults[0].ParsedText;
                    const lignes = text.split('\n');
                    
                    // Extraire tous les codes dans l'ordre d'apparition
                    const codesPage = [];
                    const numerosPage = [];
                    
                    lignes.forEach((ligne, lineIndex) => {
                        const ligneClean = ligne.trim();
                        
                        // üì± EXTRAIRE LES CODES
                        const codesMatch = ligneClean.match(/30m1G[A-Za-z0-9]{4}\b/g);
                        if (codesMatch) {
                            codesMatch.forEach(code => {
                                codesPage.push({
                                    code: code,
                                    page: pageNum,
                                    ligne: lineIndex + 1,
                                    ordreGlobal: tousLesCodesOrdonnes.length + codesPage.length
                                });
                            });
                        }
                        
                        // üî¢ EXTRAIRE LES NUM√âROS S√âPAR√âS (pour v√©rification)
                        if (ligneClean.match(/^\d+$/)) {
                            const num = parseInt(ligneClean);
                            if (num >= 1 && num <= 321) {
                                numerosPage.push({
                                    numero: num,
                                    page: pageNum,
                                    ligne: lineIndex + 1
                                });
                            }
                        }
                    });
                    
                    tousLesCodesOrdonnes.push(...codesPage);
                    numerosDetectes.push(...numerosPage);
                    
                    console.log(`   üì± ${codesPage.length} codes extraits`);
                    console.log(`   üî¢ ${numerosPage.length} num√©ros s√©par√©s d√©tect√©s`);
                    
                    // Afficher quelques codes de cette page
                    if (codesPage.length > 0) {
                        const echantillon = codesPage.slice(0, 3).map(c => c.code).join(', ');
                        console.log(`   üìã √âchantillon: ${echantillon}`);
                    }
                }
                
                // Pause entre pages
                if (i < tempFiles.length - 1) {
                    console.log('   ‚è≥ Pause de 2 secondes...\n');
                    await new Promise(resolve => setTimeout(resolve, 2000));
                }
                
            } catch (ocrError) {
                console.log(`‚ùå Erreur OCR page ${pageNum}:`, ocrError.message);
            }
        }
        
        // üìä ASSOCIATION PAR ORDRE
        console.log('\nüìä R√âSULTATS DE L\'EXTRACTION:');
        console.log(`üì± Total codes extraits: ${tousLesCodesOrdonnes.length}`);
        console.log(`üî¢ Total num√©ros d√©tect√©s: ${numerosDetectes.length}`);
        
        // Cr√©er les associations en supposant que les codes sont dans l'ordre 1, 2, 3...
        const associations = new Map();
        const codesRecuperes = [];
        const numerosManquants = [];
        
        // Associer chaque code √† son num√©ro s√©quentiel
        tousLesCodesOrdonnes.forEach((codeInfo, index) => {
            const numeroAssocie = index + 1; // Num√©ro 1, 2, 3, etc.
            if (numeroAssocie <= 321) {
                associations.set(numeroAssocie, codeInfo.code);
                codesRecuperes.push(numeroAssocie);
            }
        });
        
        // Identifier les num√©ros manquants
        for (let i = 1; i <= 321; i++) {
            if (!associations.has(i)) {
                numerosManquants.push(i);
            }
        }
        
        console.log(`\nüéØ ASSOCIATIONS CR√â√âES:`);
        console.log(`‚úÖ Codes associ√©s: ${associations.size}`);
        console.log(`‚ùå Num√©ros manquants: ${numerosManquants.length}`);
        console.log(`üéØ Taux de r√©ussite: ${((associations.size / 321) * 100).toFixed(1)}%`);
        
        // Afficher les associations cr√©√©es
        if (associations.size > 0) {
            console.log(`\nüìñ √âCHANTILLON D'ASSOCIATIONS (10 premiers):`);
            Array.from(associations.entries())
                .slice(0, 10)
                .forEach(([numero, code]) => {
                    console.log(`   ${numero} ‚Üí ${code}`);
                });
        }
        
        // Afficher les num√©ros manquants
        if (numerosManquants.length > 0) {
            console.log(`\n‚ùå NUM√âROS MANQUANTS (${numerosManquants.length}):`);
            
            // Grouper par plages
            let plages = [];
            let debut = numerosManquants[0];
            let fin = numerosManquants[0];
            
            for (let i = 1; i < numerosManquants.length; i++) {
                if (numerosManquants[i] === fin + 1) {
                    fin = numerosManquants[i];
                } else {
                    if (debut === fin) {
                        plages.push(`${debut}`);
                    } else {
                        plages.push(`${debut}-${fin}`);
                    }
                    debut = fin = numerosManquants[i];
                }
            }
            
            if (debut === fin) {
                plages.push(`${debut}`);
            } else {
                plages.push(`${debut}-${fin}`);
            }
            
            if (numerosManquants.length <= 50) {
                console.log(`üéØ Num√©ros: ${numerosManquants.join(', ')}`);
            } else {
                console.log(`üéØ Premiers 25: ${numerosManquants.slice(0, 25).join(', ')}`);
                console.log(`üéØ Derniers 25: ${numerosManquants.slice(-25).join(', ')}`);
            }
            console.log(`üìà Plages: ${plages.join(', ')}`);
        }
        
        // üìä V√âRIFICATION avec les num√©ros d√©tect√©s s√©par√©ment
        if (numerosDetectes.length > 0) {
            console.log(`\nüîç V√âRIFICATION avec num√©ros d√©tect√©s s√©par√©ment:`);
            const numerosDetectesUniques = [...new Set(numerosDetectes.map(n => n.numero))].sort((a, b) => a - b);
            console.log(`üî¢ Num√©ros uniques d√©tect√©s: ${numerosDetectesUniques.length}`);
            console.log(`üìã √âchantillon: ${numerosDetectesUniques.slice(0, 20).join(', ')}`);
            
            // Voir si l'ordre correspond
            console.log(`\nüéØ Comparaison ordre vs num√©ros d√©tect√©s:`);
            console.log(`   Codes extraits: ${tousLesCodesOrdonnes.length}`);
            console.log(`   Num√©ros d√©tect√©s: ${numerosDetectesUniques.length}`);
            console.log(`   Diff√©rence: ${Math.abs(tousLesCodesOrdonnes.length - numerosDetectesUniques.length)}`);
        }
        
        // üéØ STATISTIQUES FINALES
        console.log(`\nüìä STATISTIQUES FINALES:`);
        console.log(`üéØ Total tickets attendus: 321`);
        console.log(`‚úÖ Codes extraits et associ√©s: ${associations.size} (${((associations.size / 321) * 100).toFixed(1)}%)`);
        console.log(`‚ùå Codes non r√©cup√©r√©s: ${321 - associations.size}`);
        console.log(`üì± Total codes bruts extraits: ${tousLesCodesOrdonnes.length}`);
        
        // Nettoyage
        console.log('\nüßπ Nettoyage...');
        tempFiles.forEach(file => {
            try {
                fs.unlinkSync(path.join('./temp/', file));
            } catch (err) {}
        });
        
        return {
            associations: associations,
            numerosManquants: numerosManquants,
            totalCodesExtraits: tousLesCodesOrdonnes.length,
            codesDetails: tousLesCodesOrdonnes
        };
        
    } catch (error) {
        console.error('‚ùå Erreur:', error.message);
        return null;
    }
}

// Lancement de l'analyse
associerCodesParOrdre()
    .then(resultats => {
        if (resultats) {
            console.log(`\nüèÅ ANALYSE TERMIN√âE`);
            console.log(`\nüéØ R√âPONSE √Ä VOTRE QUESTION:`);
            console.log(`"Quels num√©ros n'ont pas √©t√© r√©cup√©r√©s dans l'extraction ?"`);
            console.log(`\n‚ùå LES ${resultats.numerosManquants.length} NUM√âROS DONT LES CODES N'ONT PAS √âT√â R√âCUP√âR√âS:`);
            
            if (resultats.numerosManquants.length <= 50) {
                console.log(`${resultats.numerosManquants.join(', ')}`);
            } else {
                console.log(`Premiers 25: ${resultats.numerosManquants.slice(0, 25).join(', ')}`);
                console.log(`Derniers 25: ${resultats.numerosManquants.slice(-25).join(', ')}`);
            }
            
            console.log(`\nüìä R√âSUM√â:`);
            console.log(`‚úÖ R√©cup√©r√©s: ${resultats.associations.size}/321 (${((resultats.associations.size / 321) * 100).toFixed(1)}%)`);
            console.log(`‚ùå Non r√©cup√©r√©s: ${resultats.numerosManquants.length}/321`);
        }
        process.exit(0);
    })
    .catch(error => {
        console.error('üí• Erreur fatale:', error.message);
        process.exit(1);
    }); 
 